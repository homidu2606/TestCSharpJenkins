using System;
using System.Collections.Generic;
using System.Text;

namespace TestCSharpJenkins
{
    public class Comparision
    {
        public bool GreaterThan(int a, int b)
        {
            return a > b;
        }

        public bool LessThan(int a, int b)
        {
            return a < b;
        }

        public bool Equal(int a, int b)
        {
            return a == b;
        }
    }
}
